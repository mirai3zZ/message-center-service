    /**
     * 设置成功返回true，不成功返回false
     *
     * @param lockKey
     * @param lockValue
     * @param time
     * @return
     */
    public Boolean setIfAbsent(String lockKey, String lockValue, long time) {
        if (redisTemplate.opsForValue().setIfAbsent(lockKey, lockKey)) {
            expire(lockKey, time);
            return true;
        }
        return false;
    }

    /**
     * 检查key值是否有效，有效设置该key的保存时间，无效线程睡眠
     *
     * @param key
     * @param second
     * @return
     */
    public boolean checkLock(String key, long second) {
        String lockKey = key;
        String ip = CommonUtils.getIpV4() ;
        // 1表示之前不存在，设置成功
        if (setIfAbsent(lockKey, ip+ "_" + String.valueOf(System.currentTimeMillis() + second))) {
            // 设置有限期
            expire(lockKey, second);
            return true;
        } else {
            //取出redis中的value和当前ip判断
            Object object = get(lockKey);
            String lockValue1 = object != null ? object.toString() : null;
            if(lockValue1!=null&&StringUtils.equals(ip,lockValue1.substring(0,lockValue1.lastIndexOf("_")))){
                return true;
            }
            //查到锁的值并与当前时间比较检查其是否已经超时，若超时则可以重新获取锁
            else if (lockValue1 != null && System.currentTimeMillis() > Long.valueOf(lockValue1.substring(lockValue1.lastIndexOf("_") + 1))) {
                //通过用当前时间戳getset操作会给对应的key设置新的值并返回旧值，这是一个原子操作
                //redis返回nil,则说明该值已经无效
                String lockValue2 = this.getset(lockKey, ip +"_"+ String.valueOf(System.currentTimeMillis() + second));
                if (lockValue2 == null || StringUtils.equals(lockValue1, lockValue2)) {
                    return true;
                } else  {
                    // 设置毫秒的延迟，避免过多请求
                    try {
                        Thread.sleep(second);
                        return false;
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }

            }
            //lockValue1 为null
            else if(null == lockValue1){
                set(lockKey+"_"+ String.valueOf(System.currentTimeMillis() + second), ip, second);
                return true;
            }
            return false;
        }
    }

    /**
     * 检查key值是否有效，有效设置该key的保存时间，无效线程睡眠
     *
     * @param key
     * @param second
     * @return
     */

    public boolean checkLockOld(String key, long second) {

        String lockKey = key;
        try {
            String ip =  CommonUtils.getIpV4() ;
            // 1表示之前不存在，设置成功
            if (setIfAbsent(lockKey, ip) == true) {
                // 设置有限期
                expire(lockKey, second);
                return true;
            } else {
                //取出redis中的value和当前ip判断
                Object object = get(lockKey);
                String lockValue2 = object != null ? object.toString() : null;
                if (lockValue2 == null) {
                    set(lockKey, ip, second);
                    return true;
                } else if (lockValue2.equals(ip)) {
                    return true;
                } else {
                    // 设置毫秒的延迟，避免过多请求
                    try {
                        Thread.sleep(second);
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }
                return false;
            }
        } catch (RedisException e) {
            e.printStackTrace();
            return true;
        }
    }
